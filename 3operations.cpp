#include <iostream>
using namespace std;

int main() {
	int x, y;
	cin >> x >> y;
	
	cout << x + y << endl;
	// сложение
	
	cout << x - y << endl;
	// вычитаение
	
	cout << x*y << endl;
	// умножение
	
	cout << x/y << endl;
	// целочисленное деление (с отбрасыванием остатка)
	
	cout << x%y << endl;
	// взятие остатка
}
