#include <iostream>
using namespace std;

int main() {
	int x, y;
	cin >> x >> y;

	if (x < y) {}
	if (x > y) {}
	if (x <= y) {}
	// меньше или равно
	if (x >= y) {}
	// больше или равно
	if (x = y) {}
	// неправильный способ, избегайте этого
	if (x == y) {}
	// сравнение
	if (x != y) {}
	// не равно

	// отрицание:
	if (!(x == y)) {}
	// не (x равно y)
}
