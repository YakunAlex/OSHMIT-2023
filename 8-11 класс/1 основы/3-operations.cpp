#include <iostream>
using namespace std;

int main() {
	int x, y;
	cin >> x >> y;
	// считали переменную x
	
	cout << x + y << endl;
	cout << x - y << endl;
	cout << x * y << endl;
	// сложение, вычитание, умножение
	cout << x / y << endl;
	// целочисленное деление (округляя вниз)
	cout << x % y << endl;
	// остаток по модулю
}
